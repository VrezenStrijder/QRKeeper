    public string Get(AndroidAppLanguage language, string key)
    {
        IReadOnlyDictionary<string, string> texts = language == AndroidAppLanguage.Chinese ? Chinese : English;
        return texts.TryGetValue(key, out string? value) ? value : key;
    }

    public string Format(AndroidAppLanguage language, string key, params object[] args)
    {
        return string.Format(Get(language, key), args);
    }
